# Ask the user for three numbers and print their total using parentheses.
no1 = int(input("Enter the first number: "))
no2 = int(input("Enter the second number: "))
no3 = int(input("Enter the third number: "))

# Calculate the total using parentheses
total = (no1 + no2) + no3
print(f"The total using parentheses is: {total}")
